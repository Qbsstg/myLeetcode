    public static double findMaxAverage(int[] nums, int k) {

        double pre = 0.0;

        for (int i = 0; i < k; i++) {
            pre = pre + nums[i];
        }
        double max = pre;
        for (int i = k; i < nums.length; i++) {
            pre = pre + nums[i] - nums[i - k];
            max = Math.max(pre, max);
        }
        return max / k;

    }


